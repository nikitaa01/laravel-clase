     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validated = Validator::make(
            $request->all(),
            [
                'username' => 'required'
            ]
        );
        if ($validated->fails()) {
            return new JsonResponse([
                'errors' => $validated->errors()
            ], 400);
        }
        $response = [
            'request' => $request->all(),
            'id' => $id
        ];
        return new JsonResponse($response, 200);
    }

